char	*ft_strcapitalize(char *str)
{
	int i;
	int j;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] > 64 && str[i] < 91)
		{
			str[i] = str[i] + 32;
		}
		i++;
	}
	j = 0;
	while (str[j] != '\0')
	{
		if (str[0] > 96 && str[0] < 123)
			str[0] = str[0] - 32;
		if ((str[j] > 96 && str[j] < 123) && ((str[j - 1] < 97
			&& str[j - 1] > 90) || (str[j - 1] < 65 && str[j - 1] > 57)
			|| str[j - 1] < 48 || str[j - 1] > 122))
			str[j] = str[j] - 32;
		j++;
	}
	return (str);
}
