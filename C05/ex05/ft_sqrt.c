int	ft_sqrt(int nb)
{
	int res;
	int n;

	res = 1;
	n = 0;
	if (nb <= 0)
		return (0);
	while (res <= nb / 2)
	{
		n = res * res;
		if (n == nb)
			return (res);
		res++;
	}
	return (0);
}
